varOne = 10
varTwo = 10

# > >= < <= == !=
print("Greater than", varOne > varTwo)
print("Greater than or equal", varOne >= varTwo)
print("Less than", varOne < varTwo)
print("Less than  or equal", varOne <= varTwo)
print("Equals to", varOne == varTwo)
print("Not Equals to", varOne != varTwo)